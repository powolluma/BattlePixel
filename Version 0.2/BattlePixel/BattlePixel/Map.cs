namespace BattlePixel
{
    /// <summary>
    /// Класс игровой карты. Хранит данные о тайлах в двумерном массиве
    /// и отвечает за генерацию и рендеринг карты через TileManager.
    /// </summary>
    public class GameMap
    {
        private readonly TileManager _tileManager; // Менеджер для рендеринга тайлов
        private readonly int[,] _mapData;          // Двумерный массив карты: [строка, колонка]

        // ================================================================
        //  КОНСТРУКТОР
        //  Создает пустую карту заданного размера
        // ================================================================
        public GameMap(TileManager tileManager, int width, int height)
        {
            _tileManager = tileManager;
            _mapData = new int[height, width]; // Инициализация массива карты
        }

        // ================================================================
        //  ГЕНЕРАЦИЯ ТЕСТОВОЙ КАРТЫ
        //  Создает простую карту с землей в нижнем ряду
        //  Остальные ячейки остаются пустыми (-1)
        // ================================================================
        public void GenerateTestMap()
        {
            int width = _mapData.GetLength(1);  // Количество колонок
            int height = _mapData.GetLength(0); // Количество строк

            // Заполнение всей карты пустыми ячейками (-1 означает отсутствие тайла)
            for (int y = 0; y < height; y++)
                for (int x = 0; x < width; x++)
                    _mapData[y, x] = -1;

            int groundY = height - 1; // Последняя строка - это уровень земли

            // Заполнение нижнего ряда тайлами земли (ID = 1)
            for (int x = 0; x < width; x++)
            {
                _mapData[groundY, x] = 1; // Тайл земли
            }
        }

        // ================================================================
        //  РЕНДЕРИНГ КАРТЫ
        //  Проходит по всем ячейкам карты и отображает каждый тайл
        //  через TileManager, если ячейка не пустая
        // ================================================================
        public void Render()
        {
            for (int y = 0; y < _mapData.GetLength(0); y++) // Проход по строкам
            {
                for (int x = 0; x < _mapData.GetLength(1); x++) // Проход по колонкам
                {
                    int id = _mapData[y, x]; // ID тайла в ячейке
                    if (id >= 0) // Если ячейка не пустая (-1)
                        _tileManager.PlaceTile(id, x, y); // Отображение тайла
                }
            }
        }
    }
}